#include <iostream>
using namespace std;
 
int main (){
	
	int x = 5;
	int y = 3;
	
	cin >> x;
	cin >> y;
	
	
	cout << (x <= y) << "\n";
	cout << (x >= y) << "\n";
	cout << (x < y) << "\n";
	cout << (x > y) << "\n";
	cout << (x != y) << "\n";
	cout << (x == y) << "\n";
	
	return 0;
}